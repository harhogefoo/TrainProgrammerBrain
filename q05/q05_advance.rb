
coins = [10,50,100,500]
cnt = 0
(2..15).each do |i|
  coins.repeated_combination(i).each{|coin_set|
    # ex) i = 2
    # [10,10],[10,50],[10,100],[10,500]
    # [50,10],[50,50],[50,100],[50,500]
    # [100,10],[100,50],[100,100],[100,500]
    # [500,10],[500,50],[500,100],[500,500]

    cnt += 1 if coin_set.inject(:+) == 1000
    # ex) [1,2,3].inject(:+) =  6

  }
end
puts cnt